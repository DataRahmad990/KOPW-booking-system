// Group staff by jabatan for optgroup display
export function getStaffByJabatan(): Record<string, StaffMember[]> {
  const grouped: Record<string, StaffMember[]> = {};
  for (const staff of staffList) {
    if (!grouped[staff.jabatan]) {
      grouped[staff.jabatan] = [];
    }
    grouped[staff.jabatan].push(staff);
  }
  return grouped;
}
